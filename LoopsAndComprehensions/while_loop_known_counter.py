counter_up = 0

print("\n counting upwards:")

while counter_up <= 5:
    print("The counter's value is {}".format(counter_up))
    counter_up +=1

print("\n\n counting downwards: ")

counter_down = 5

while counter_down >= 0:
    print("The counter's value is {}".format(counter_down))
    counter_down -= 1

