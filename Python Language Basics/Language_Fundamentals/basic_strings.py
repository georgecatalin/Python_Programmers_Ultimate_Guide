print("Strings - examples 1") # this is an example of a simple string

# string concatenation
print("hello" + "world")

# multiline strings
print("""
this is a
multiline string
and it prints out exactly :->
as it is
""")

#  special characters
print("\nHello \tWorld Python")

# string multiplication
print("Hello, Mara" * 10)

# how to escape quotations
print("Hello 'George' ")
print('Hello "George" ')
print("Hello \"George\"")
print("Hello ""George"" ")

# raw strings => they are printed exactly as they are, escape characters are not accounted for
print(r"\nThis is a \"raw string" )

# print multiple items
print("Hello", "Mara", 2011)